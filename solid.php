2) Open Closed -> Sınıf ya da bir fonksiyon için var olan özellikleri korur değişikliğe izin vermez.


3)Liskov Substution  -> Kodlarda değişiklik yapmadan bir sınıfın başka bir sınıfın özelliklerini kullanabilir.

4) Interface Segregation -> Bir sınıfın çok farklı özellikleri varsa, bu sınıfın çok farklı özellikleri için bir arayüz oluşturun.

5) Dependency Inversion -> Sınıflar arası bağımlılk olabildiğince az olmalı üst seviye sınıf alt seviye sınıflara bağlı olmalı. 

